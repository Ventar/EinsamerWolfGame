                Sommerlund und seiner Hauptstadt Holmgard. Weiterhin zeigt sie
                auch das im Osten liegende Reich Durenor sowie die Ödlande, die
                zwischen diesen beiden Königreichen liegen.
              </p>
              <h4>Wie die Ausrüstung getragen wird</h4>
              <h5>Waffen</h5>
              <p>
                Waffen benötigst du im Kampf. Wenn du die Kai-Disziplin der
                Waffenkunde beherrschst und die entsprechende Waffe besitzt,
                kannst du im Kampf 2 Punkte zu deiner KAMPFSTÄRKE hinzurechnen.
                Solltest du in einen Kampf geraten, ohne irgendeine Waffe zu
                besitzen, musst du mit bloßen Händen kämpfen und dir 4 Punkte
                von deiner KAMPFSTÄRKE abziehen. Wenn du während deines
                Abenteuers eine Waffe findest, kannst du sie mitnehmen. Benutzen
                kannst du im Kampf aber immer nur eine deiner Waffen. Du kannst
                insgesamt zwei Waffen während deines Abenteuers mit dir führen.
                Die meisten Waffen werden entweder in der Hand oder am Gürtel
                getragen. Du kannst Waffen zu jedem Zeitpunkt, in dem du dich
                nicht in einem Kampf befindest, ablegen oder wechseln.
              </p>
              <h5>Rucksackgegenstände</h5>
              <p>
                Während deiner Reise wirst du zahlreiche nützliche Gegenstände
                finden, die du möglicherweise mit dir nehmen möchtest. Beachte,
                dass du nur acht Gegenstände in deinem Rucksack tragen kannst.
                Falls du zu irgendeinem Zeitpunkt deinen Rucksack verlieren
                solltest, so sind automatisch auch alle deine
                Rucksackgegenstände verloren. Ohne Rucksack ist es dir
                unmöglich, Rucksackgegenstände mit dir zu führen.
              </p>
              <h5>Spezielle Gegenstände</h5>
              <p>
                Jeder spezielle Gegenstand hat einen eigenen Zweck und Effekt.
                Dieser Effekt wird entweder erläutert, wenn du den Gegenstand
                erhältst, oder du findest ihn im Laufe deines Abenteuers heraus.
              </p>
              <h5>Goldkronen</h5>
              <p>
                Die Währung in Sommerlund ist die Krone, eine kleine Goldmünze.
                Mit Goldkronen kannst du während deines Abenteuers
                Ausrüstungsgegenstände, Essen und Übernachtungen bezahlen und
                sie sogar als Bestechungsgeld benutzen. Manche Gegner, auf die
                du triffst, werden Goldkronen besitzen, oder du kannst welche
                finden, während du etwas untersuchst. Alle deine Goldkronen
                werden in deinem Lederbeutel aufbewahrt, in dem du bis zu 50
                Goldkronen tragen kannst.
              </p>
              <h5>Proviant</h5>
              <p>
                Während deines Abenteuers musst du regelmäßig essen. Solltest du
                nicht essen, wenn es dir im Text vorgeschrieben wird, verlierst
                du 3 AUSDAUERPUNKTE. Wenn du die Kai-Disziplin „Jagd“ als eine
                deiner fünf Disziplinen gewählt hast, bist du nicht dazu
                gezwungen, eine Mahlzeit einzunehmen, wenn im Text davon die
                Rede ist.
              </p>
              <h5>Heiltränke</h5>
              <p>
                Diese geben dir (pro Anwendung) 4 Punkte deiner AUSDAUER zurück,
                solltest du sie z. B. in einem Kampf verloren haben. Wenn du
                während deines Abenteuers andere Tränke findest, wird dir ihre
                Wirkung im Text erklärt. Alle Tränke und Heiltränke sind
                Rucksackgegenstände.
              </p>
