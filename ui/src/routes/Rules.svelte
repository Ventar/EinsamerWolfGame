              <p>
                Deine Basisausrüstung besteht aus einer grünen Tunika und einem
                Kai-Umhang. Weiterhin besitzt du einen Rucksack, einen Gürtel
                und einen Lederbeutel, in dem du dein Geld (Goldkronen)
                aufbewahrst. Um zu sehen, wie viel Geld du besitzt, musst du
                eine Zahl von der Zufallszahlen-Tabelle ermitteln. Das Ergebnis
                entspricht der Anzahl von Goldkronen, mit denen du dein
