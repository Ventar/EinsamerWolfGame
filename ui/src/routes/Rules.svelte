              <h4>Gedankensperre</h4>
              <p>
                Einige deiner Feinde haben die Fähigkeit, dich mit ihrer
                mentalen Kraft anzugreifen. Die Kai-Disziplin der Gedankensperre
                kann dich vor dem Verlust von AUSDAUERPUNKTEN bewahren, wenn du
                dieser Art eines Angriffs ausgesetzt bist. Wenn du diese
                Fertigkeit wählst, schreibe „Gedankensperre: Kein
                AUSDAUER-Verlust bei Angriffen durch den Gedankenstrahl“ auf
                dein Aktionsblatt.
              </p>
              <h4>Gedankenstrahl</h4>
              <p>
                Der Gedankenstrahl ermöglicht es einem Kai-Lord, seinen Gegner
                mit der Kraft seines Geistes anzugreifen. Dies kann er während
                eines normalen, mit Waffen geführten Kampfes tun und so seine
                KAMPFSTÄRKE für die Dauer des Kampfes um 2 Punkte erhöhen. Nicht
                alle Kreaturen, denen man in diesem Abenteuer begegnet, können
                durch den Gedankenstrahl verletzt werden. Du wirst erfahren,
                falls eine Kreatur immun dagegen ist. Wenn du diese Fertigkeit
                wählst, schreibe „Gedankenstrahl: +2 KAMPFSTÄRKEPUNKTE“ auf dein
                Aktionsblatt.
              </p>
              <h4>Tierverständnis</h4>
              <p>
                Diese Fertigkeit ermöglicht es einem Kai-Lord, mit einigen
                Tieren zu sprechen und ihr Verhalten zu verstehen. In einigen
                Fällen gibt sie einem Kai-Lord sogar die Kontrolle über die
                Wahrnehmung und das Handeln eines Tieres. Wenn du diese
                Fertigkeit wählst, so schreibe „Tierverständnis“ auf dein
                Aktionsblatt.
              </p>
              <h4>Geist über Materie</h4>
              <p>
                Die Beherrschung dieser Disziplin ermöglicht es einem Kai-Lord,
                kleine Objekte durch pure Konzentration zu bewegen. Wenn du
                diese Fertigkeit wählst, schreibe „Geist über Materie“ auf dein
                Aktionsblatt.
              </p>
