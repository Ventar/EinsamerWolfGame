            <div class="accordion-body">
              <p>
                Über die Jahrhunderte haben die Kai-Mönche eine Reihe nützlicher
                Fähigkeiten vervollkommnet und es darin zur Perfektion gebracht.
                Diese Fertigkeiten, in denen jeder Kai-Mönch unterwiesen wird,
                werden „Kai-Disziplinen“ genannt. Du hast bis jetzt allerdings
                erst fünf der weiter unten aufgelisteten Disziplinen erlernt.
                Welche diese fünf Fertigkeiten sind, liegt in deiner Hand.
              </p>
              <p>
                Da alle der unten aufgeführten Disziplinen an irgendeinem Punkt
                deiner gefahrvollen Reise von entscheidender Bedeutung sein
                können, solltest du deine Wahl mit Bedacht treffen. Die
                geeignete Disziplin, zur rechten Zeit angewandt, kann dein Leben
                retten.
              </p>
              <p>
                Wenn du die Wahl deiner fünf Disziplinen getroffen hast, notiere
                sie in der entsprechenden Rubrik auf deinem Aktionsblatt.
              </p>
              <h4>Tarnung</h4>
              <p>
                Diese Disziplin ermöglicht es einem Kai-Lord, mit seiner
                Umgebung zu verschmelzen. In der Natur kann er sich unentdeckt
                zwischen Bäumen und Felsen bewegen und sich einem Gegner nähern,
                ohne von diesem bemerkt zu werden. In einem Dorf oder einer
                Stadt hat er die Möglichkeit, das Aussehen und die Sprache eines
                Einheimischen anzunehmen, unterzutauchen oder einen sicheren Ort
                ausfindig zu machen. Wenn du diese Fertigkeit wählst, schreibe
                „Tarnung“ auf dein Aktionsblatt.
              </p>
              <h4>Jagd</h4>
              <p>
                Diese Fertigkeit sorgt dafür, dass ein Kai-Lord in der Wildnis
                niemals Hunger leiden muss. Er wird immer in der Lage sein,
                Nahrung für sich zu jagen, außer im Ödland oder in der Wüste.
                Diese Fertigkeit ermöglicht es einem Kai-Lord außerdem, sich
                möglichst schnell und leise zu bewegen. Wenn du diese Fertigkeit
                wählst, schreibe „Jagd: Keine Mahlzeit erforderlich, wenn
                gegessen werden muss“ auf dein Aktionsblatt.
              </p>
              <h4>Sechster Sinn</h4>
              <p>
                Diese Fertigkeit kann einen Kai-Lord vor drohender Gefahr
                warnen. Sie kann auch Aufschluss über die wahren Absichten eines
                Fremden oder den Nutzen eines unbekannten Objekts geben. Wenn du
                diese Fertigkeit wählst, schreibe „Sechster Sinn“ auf dein
                Aktionsblatt.
              </p>
              <h4>Spurensuche</h4>
              <p>
                Diese Disziplin ermöglicht es einem Kai-Lord, sich in der
                Wildnis für den richtigen Weg zu entscheiden, den Aufenthaltsort
                einer Person oder eines Objekts in einer Stadt zu bestimmen und
                die Geheimnisse von Fußspuren und Fährten zu entschlüsseln. Wenn
                du diese Fertigkeit wählst, schreibe „Spurensuche“ auf dein
                Aktionsblatt.
              </p>
              <h4>Heilung</h4>
              <p>
                Diese Fertigkeit kann eingesetzt werden, um verlorene
                AUSDAUERPUNKTE zurückzugewinnen. Wenn du diese Fertigkeit
                besitzt, kannst du dir für jeden nummerierten Textabschnitt, den
                du im Spiel passierst und in dem du dich nicht in einem Kampf
                befindest, 1 AUSDAUERPUNKT gutschreiben. Denke immer daran, dass
                du nur AUSDAUERPUNKTE zurückgewinnen kannst, die du verloren
                hast, du deinen Anfangswert also niemals übersteigen darfst.
                Wenn du diese Fertigkeit wählst, schreibe „Heilung: + 1
                AUSDAUERPUNKT für jeden Abschnitt ohne Kampf“ auf dein
                Aktionsblatt.
              </p>
              <h4>Waffenkunde</h4>
              <p>
                Ab dem Eintritt in die Kai-Abtei wird jeder Initiand darin
                geschult, einen Waffentyp meisterlich zu beherrschen. Wenn die
                Waffenkunde zu deinen Kai-Disziplinen gehört, dann ermittle eine
                Zufallszahl und suche anschließend aus der Liste weiter unten
                die Waffe heraus, die der ermittelten Zahl entspricht. Das ist
                die Waffe, mit der du ausgebildet worden bist. Wenn du mit
                dieser Waffe in der Hand in einen Kampf ziehst, erhöht sich
                deine KAMPFSTÄRKE für die Dauer des Kampfes um 2 Punkte. Die
                Ausbildung an einer Waffe bedeutet jedoch nicht, dass du das
                Abenteuer auch mit dieser Waffe beginnst. Allerdings wirst du im
                Lauf deines Abenteuers immer wieder die Möglichkeit haben,
                Waffen zu finden. Du darfst jedoch nie mehr als 2 Waffen mit dir
                führen und du kannst immer nur eine Waffe auf einmal benutzen.
                Wenn du diese Fertigkeit wählst, schreibe „Waffenkunde mit
                (Waffe): +2 KAMPFSTÄRKEPUNKTE“ auf dein Aktionsblatt.
              </p>
