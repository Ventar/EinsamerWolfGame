              <p>
                Es wird während deines Abenteuers Situationen geben, in denen du
                gezwungen bist gegen einen Gegner zu kämpfen. Die KAMPFSTÄRKE
                und AUSDAUER deines Gegners kannst du in solch einem Moment dem
                Text des entsprechenden Abschnitts entnehmen. Dein Ziel als
                Einsamer Wolf ist es, die AUSDAUER deines Gegners während des
                Kampfes auf null zu reduzieren und dabei selbst möglichst wenige
                AUSDAUERPUNKTE zu verlieren. Trage zu Beginn eines Kampfes deine
                AUSDAUERPUNKTE und die deines Gegners in die vorgegebenen
                Kästchen des Kampfprotokolls auf dem Aktionsblatt ein.
              </p>
              <h5>Ablauf des Kampfes</h5>
              <ol>
                <li>
                  <p>
                    Addiere sämtliche Extrapunkte, die du durch deine Kai-
                    Disziplinen und Waffen erhältst, zu deiner aktuellen
                    KAMPFSTÄRKE hinzu.
                  </p>
                </li>
                <li>
                  <p>
                    Ziehe von diesem Wert die KAMPFSTÄRKE deines Gegners ab. Das
                    Ergebnis ist der sogenannte „Kampfquotient“. Trage diesen
                    Kampfquotienten in das Aktionsblatt ein.
                  </p>
                  <p>
                    <strong>Beispiel</strong>
                  </p>
                  <p>
                    Einsamer Wolf (KAMPFSTÄRKE 15) wird von einem
                    Schwingenteufel (KAMPFSTÄRKE 20) überrascht. Er hat nicht
                    die Möglichkeit, zu fliehen, und muss sich daher dem Kampf
                    stellen, als die Kreatur auf ihn herabstößt. Einsamer Wolf
                    beherrscht die Kai-Disziplin Gedankenstrahl, gegen die der
                    Schwingenteufel nicht immun ist. Einsamer Wolf erhält daher
                    2 Punkte zu seiner aktuellen KAMPFSTÄRKE dazu, was einen
                    Gesamtwert von 17 Punkten ergibt. Hiervon zieht er nun die
                    20 Punkte KAMPFSTÄRKE des Schwingenteufels ab, was einen
                    Kampfquotienten von -3 ergibt (17 - 20 = -3). Auf dem
                    Aktionsblatt wird als Kampfquotient -3 notiert.
                  </p>
                </li>
                <li>
                  <p>
                    Wenn der Kampfquotient feststeht, muss nun noch eine
                    Zufallszahl aus der Zufallszahlentabelle ermittelt werden.
                  </p>
                </li>

                <li>
                  <p>
                    Benutze nun die Kampfresultat-Tabelle am Ende des Buches. Am
                    oberen Rand sind die Kampfquotienten angegeben und am
                    senkrechten Rand stehen die Zufallszahlen. Finde das Feld,
                    wo sich dein Kampfquotient und die ermittelte Zufallszahl
                    überschneiden. Dort siehst du die Anzahl der AUSDAUERPUNKTE,
                    die Einsamer Wolf und sein Gegner in dieser Kampfrunde
                    verlieren (ein F zeigt den Verlust für den Feind und EW den
                    Verlust für Einsamer Wolf an).
                  </p>
                  <p>
                    <strong>Beispiel</strong>
                  </p>
                  <p>
                    Der Kampfquotient zwischen Einsamer Wolf und dem
                    Schwingenteufel ist -3. Wenn nun die Zufallszahl eine 6 ist,
                    dann ist das Resultat der ersten Kampfrunde wie folgt:
                  </p>
                  <p>Einsamer Wolf verliert 3 AUSDAUERPUNKTE.</p>
                  <p>Der Schwingenteufel verliert 6 AUSDAUERPUNKTE.</p>
                </li>
                <li>
                  <p>
                    Vermerke die Änderungen der AUSDAUERPUNKTE auf dem
                    Kampfprotokoll.
                  </p>
                </li>
                <li>
                  <p>
                    Wenn es keine anderen Anweisungen gibt oder du keine Chance
                    zur Flucht hast, beginnt nun die nächste Kampfrunde.
                  </p>
                </li>
                <li><p>Wiederhole die Schritte ab Punkt 3.</p></li>
              </ol>
              <p>
                Dieser Ablauf wird so oft wiederholt, bis die AUSDAUERPUNKTE des
                Feindes oder von Einsamer Wolf auf 0 sinken, was den Tod
                desjenigen bedeutet. Wenn Einsamer Wolf stirbt, ist das
                Abenteuer damit zu Ende. Stirbt der Gegner, kann das Abenteuer
                für Einsamer Wolf weitergehen, allerdings mit weniger
                AUSDAUERPUNKTEN, falls er im Kampf welche verloren haben sollte.
                Eine Zusammenfassung der Kampfregeln findest du am Ende des
                Buches.
              </p>
              <h5>Einem Kampf ausweichen</h5>
              <p>
                Während deines Abenteuers wirst du manchmal die Gelegenheit
                bekommen, einem Kampf auszuweichen. Wenn du dich bereits im
                Kampf befindest und dich zur Flucht entscheidest, ermittle das
                Kampfergebnis für diese Kampfrunde wie gewohnt. Alle Verluste
                des Feindes werden in diesem Fall ignoriert. Nur Einsamer Wolf
                verliert in dieser Runde AUSDAUERPUNKTE, kann aber dafür den
                Rückzug antreten.
              </p>
              <p>
                Bedenke, dass eine Flucht nur möglich ist, wenn der Text dies
                vorsieht.
              </p>
