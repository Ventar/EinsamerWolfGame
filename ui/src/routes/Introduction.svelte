  <div class="card-body">
    <h4 class="card-title">Was bisher geschah...</h4>
    <p>
      Weit im Norden liegt das Land Sommerlund. Hier ist es seit Jahrhunderten Brauch, männliche Nachkommen mit Erreichen des siebten Lebensjahres in das
      Kloster der Kai zu entsenden, wo sie in den Fertigkeiten und Disziplinen ihrer edlen Vorväter unterwiesen werden. /p>
    </p>
    <p>
      Die Kampfmönche der Kai sind Meister auf ihrem Gebiet. Ihre Zöglinge lieben und respektieren sie, trotz der Härte, die die Ausbildung mit sich bringt.
      Nach Jahren der Prüfungen kehren die Schüler als ausgebildete Kai-Krieger in ihre Heimat zurück – gewappnet im Körper sowie auch im Geiste, um sich der
      dauernden Gefahr des Krieges zu stellen, die von den Schwarzen Lords im Westen ausgeht.
    </p>
    <p>
      Vor langer Zeit, während des Zeitalters des schwarzen Mondes, brachten die Schwarzen Lords Krieg über Sommerlund. Dieser Konflikt war ein langer und
      erbitterter Machtkampf, der mit dem Sieg der Sommerlendinge in der großen Schlacht an der Maakenschlucht endete. König Ulnar – seinerseits selbst
      Kai-Meister – zersprengte gemeinsam mit seinen Verbündeten aus Durenor die Armeen der Schwarzen Lords am Pass von Moytura und zwang den Gegner zurück in
      den bodenlosen Abgrund der Maakenschlucht. Vashna, der mächtigste unter den Schwarzen Lords, fiel durch König Ulnars magisches Schwert, das den Namen
      Sommerswerd – das Schwert der Sonne – trägt. Seit dieser Zeit trachten die Schwarzen Lords nach Rache am Hause Ulnar und an ganz Sommerlund.
    </p>
    <p>
      Nun ist der Abend vor dem Tag des Fehmarnfests, an welchem der Frühlingsanfang gefeiert wird. Nach alter Tradition versammeln sich die Kai-Mönche an
      diesem Tag in ihrer Abtei am Fuße des mächtigen Schroffsteingebirges. Dort bekunden sie Treue zu ihrem Gott Kai, zum Volke der Sommerlendinge und zum
      Hause Ulnar. Viele Kai- Wanderer und Gesandte sind weit gereist, um an den Neujahrsfeierlichkeiten an Fehmarn teilnehmen zu können
    </p>
    <p>
      Du bist ein junger Kai-Schüler namens Lautloser Wolf. Diesen Namen gab dir einst dein Kai-Meister, als du vor längerer Zeit in diesen ehrenvollen Orden
      eingeweiht wurdest. Die Hälfte deines Lebens bist du nun schon von zu Hause fort, und es ist das achte Jahr, in dem du an den Fehmarnfeierlichkeiten
      teilnimmst. Auch dieses Mal bist du voller Stolz und Begeisterung. Du bist noch genauso aufgeregt wie an jenem Tag, an dem du dein Heimatdorf Darge
      verlassen hast.
    </p>
    <p>
      Die letzten Tage waren für dich wie auch für die übrigen Anwärter sehr spannend. Ältere Kai trafen in der Abtei ein und berichteten von gewonnenen
      Schlachten und Heldentaten in fernen Ländern. Jeder in der Abtei ist voller Freude und Hoffnung, dass auch das kommende Jahr Frieden und Wohlstand für die
      geliebte Heimat bringen wird. In der Abtei herrscht reges Treiben, da gerade die letzten Vorbereitungen für das morgige Fest getroffen werden. Für dich
      und die übrigen Anwärter ändert sich jedoch nichts an der täglichen Routine. Dein Training und die Ausbildung verlaufen wie gewohnt, obwohl es dir äußerst
      schwer fällt, dich auf den Unterricht deines Kai-Meisters Sternenfeuer zu konzentrieren, der euch in der Geografie der Hinterlande unterweist. „Lautloser
      Wolf!“, brüllt er dich an und weckt dich damit unsanft aus deinen Tagträumen. „Würdest du uns freundlicherweise die Namen sämtlicher Küstenstädte der
      Ödlande aufzählen?!“ Du stutzt. Die unerwartete Frage deines Lehrers lässt dich erstarrt. Mit offenem Mund blickst du ihn an.
    </p>
    <p>
      „Das dachte ich mir!“, sagt er und schüttelt missbilligend den Kopf. „Es ist nicht das erste Mal, dass mir deine Unaufmerksamkeit auffällt. Eine Strafe
      wird dir sicher helfen, dich in Zukunft besser zu konzentrieren.“
    </p>
    <p>
      Sternenfeuer greift in seine Tunika und holt eine glänzende Münze hervor. Er schaut sie einen Moment lang an, während er sich deine Strafe überlegt.
      Schließlich sagt er: „Über deine Strafe werde ich das Schicksal entscheiden lassen."
    </p>
    <p>
      Er wirft die Goldmünze in die Luft und fängt sie geschickt mit dem Rücken seiner linken Hand auf. Mit einem Blick auf das frisch geprägte Konterfei König
      Ulnars V. sagt er laut: „So sei es! Ein zusätzliches Waffentraining für dich – morgen früh, eine Stunde vor Sonnenaufgang. Du wirst dich bei mir melden –
      ausgerüstet und bereit für das Training. Ist das klar?
    </p>
    <p>„Ja, Meister“, erwiderst du kleinlaut.</p>
    <p>
      Draußen auf dem Gang erklingt die Glocke drei Mal und zeigt damit das Ende der heutigen Lektion an. Gerade packst du deine Bücher und Unterlagen in deinen
      Rucksack, um das Klassenzimmer zu verlassen, als Kaimeister Sternenfeuer noch einmal nach dir ruft:
    </p>
    <p>
      „Sei pünktlich, Lautloser Wolf, sonst wirst du Fehmarn damit verbringen, Brennholz für die Küche zu sammeln. Kommst du zu spät, werde ich persönlich dafür
      sorgen, dass du deinen Platz an der Festtafel verlierst.“
    </p>
    <p>Die Drohung, das Fest zu versäumen, trifft dich schwer. „Ja, Herr“, antwortest du. „Ich … ich werde pünktlich sein!“</p>
